<h3>Mikkelipuiston kehittämisessä ja toiminnan monipuolistamisessa otetaan iso harppaus eteenpäin uuden mallipuutarha-alueelle rakennettavan palvelurakennuksen myötä.</h3>
<br>
<img class="img-responsive" src="../assets/images/pr.png" alt="">
<br>
<h4>Uuden rakennuksen rakentaminen on alkanut jo ja rakennus on valmiina kesäkuun 2017 lopussa, sopivasti juuri ennen asuntomessujen 2017 avautumista 14. heinäkuuta.</h4>
<h4>Puutarhamatkailukohteen kymmenvuotisen toiminnan aikana Mikkelipuistossa on kaipailtu monipuoliseen toimintaan taipuvaa sisätilaa, joka mahdollistaa esimerkiksi erilaisten tapahtumien, näyttelyjen,
 jäsenyritysten asiakasiltojen ja muun toiminnan järjestämisen myös katon alla. Uusi palvelurakennus mahdollistaa Mikkelipuiston kehittämistä jatkossa monin tavoin pidentäen kautta ja avaten runsaasti uusia toimintamahdollisuuksia.</h4>
<h4>Uusi puurakenteinen rakennus on mikkeliläisen Arkkitehtitoimisto Heikki Kirjalaisen suunnittelema, ja sen rakentamisesta keskeisessä roolissa ovat Etelä-Savon ammattiopiston talotekniikan opiskelijat. Rakennuksen salitila toimii niin myymälä- ja näyttelytilana kuin kahvila-ravintolana.
 Uuteen rakennukseen saadaan myös tarvittavaa henkilöstön toimistotilaa ja vuokrattavaa kokoustilaa. Rakennuttajana hankkeessa toimii Mikkelipuistoyhdistys ry. Etelä-Savon Ely-keskus on myöntänyt hankkeelle toimintaympäristön kehittämisavustusta.</h4>
<h4>Mikkelipuisto on kesän asuntomessujen virallinen oheiskohde.</h4>
